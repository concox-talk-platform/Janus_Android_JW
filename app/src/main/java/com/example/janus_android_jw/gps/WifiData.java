package com.example.janus_android_jw.gps;

public class WifiData {
    private int firstWifi;
    private int secondWifi;
    private int thirdWifi;

    public int getFirstWifi() {
        return firstWifi;
    }

    public void setFirstWifi(int firstWifi) {
        this.firstWifi = firstWifi;
    }

    public int getSecondWifi() {
        return secondWifi;
    }

    public void setSecondWifi(int secondWifi) {
        this.secondWifi = secondWifi;
    }

    public int getThirdWifi() {
        return thirdWifi;
    }

    public void setThirdWifi(int thirdWifi) {
        this.thirdWifi = thirdWifi;
    }
}
