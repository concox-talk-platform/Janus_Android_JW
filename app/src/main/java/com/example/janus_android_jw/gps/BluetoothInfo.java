package com.example.janus_android_jw.gps;

public class BluetoothInfo {

    private int firstBt;
    private int secondBt;
    private int thirdBt;

    public int getFirstBt() {
        return firstBt;
    }

    public void setFirstBt(int firstBt) {
        this.firstBt = firstBt;
    }

    public int getSecondBt() {
        return secondBt;
    }

    public void setSecondBt(int secondBt) {
        this.secondBt = secondBt;
    }

    public int getThirdBt() {
        return thirdBt;
    }

    public void setThirdBt(int thirdBt) {
        this.thirdBt = thirdBt;
    }
}
